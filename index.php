        function escapeHtml_footer(text) {

            if (!text) return text;

            var map = {
                '&': '&amp;',
                '<': '&lt;',
                '>': '&gt;',
                '"': '&quot;',
                "'": '&apos;'
            };
            return text.toString().replace(/[&<>"']/g, function(m) {
                return map[m];
            });
        }


