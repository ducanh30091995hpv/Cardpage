        // Dashboard notification will notify the user on changes in the dashboard 
        function getDashboardnotifications() {

            // If the user is not active in the wizard right now we not running this AJAX
            if (userActive_isActive == false) return;

            // Count the notification number via Ajax
            $.post('/versions/' + versionNUM + '/wizard/include/websiteNotificationsV_beta_V4.php', {
                w: websiteID,
                type: 'total'
            }).done(function(data) {

                // prevent JS error in case the JSON didn't returned
                try {
                    data = JSON.parse(data);
                } catch (e) {}

                if (!data || typeof data !== "object") return;
                // check if the user package changed if it did will alert him to refresh
                if (data.packageNUM != packageNUM && !window.ignorePackageChangedMessage && !isBsModalOpen('#packageChangedModal')) {
                    var packageChangedModal = bootbox.confirm({
                        title: '<span><b>' + translations.packageChangeTitle + '</b></span>',
                        message: translations.packageChangeMessageDashboard,
                        buttons: {
                            confirm: {
                                label: translations.yes,
                                className: 'btn-success'
                            },
                            cancel: {
                                label: translations.no,
                                className: 'btn-default'
                            }
                        },
                        callback: function(result) {
                            // refreshing the page 
                            if (result == true) {
                                window.location.reload();
                                // update flag ignore package changed message because the user click on No button
                            } else if (result == false) {
                                window.ignorePackageChangedMessage = true;
                            }
                        }
                    });
                    // add ID on the package changed modal
                    packageChangedModal.on("shown.bs.modal", function() {
                        packageChangedModal.attr("id", "packageChangedModal");
                    });
                }
            });
        }

